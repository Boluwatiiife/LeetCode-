// 1576. Replace All ?'s to Avoid Consecutive Repeating Characters

const modifyString = function (s) {
  let word = s.split("");
  let letters = "abcdefghijklmnopqrstuvwxyz";

  for (let i = 0; i < word.length; i++) {
    if (word[i] === "?") {
      for (char of letters) {
        if (
          (i > 0 && word[i - 1] === char) ||
          (i > 0 && word[i + 1] === char)
        ) {
          continue;
        }
        word[i] = char;
      }
    }
  }
  return word.join("");
};

