// 1018. Binary Prefix Divisible By 5

const prefixesDivBy5 = function (nums) {
  let arr = [];
  for (let i = 0; i < nums.length; i++) {
    nums[i] = nums[i].toString();
  }
  arr.push(nums[0]);
  let xx = nums[0];
  for (let i = 1; i < nums.length; i++) {
    xx = xx + nums[i];
    arr.push(xx);
  }
  for (let i = 0; i < arr.length; i++) {
    arr[i] = BigInt(`0b${arr[i]}`);
    arr[i] = arr[i] % 5n === 0n;
  }

  return arr;
};

const divisible_5 = function (nums) {
  const n = nums.length;
  const answer = new Array(n);
  let remainder = 0;

  for (let i = 0; i < n; i++) {
    remainder = ((remainder << 1) | nums[i]) % 5;
    answer[i] = remainder === 0;
  }
  return answer;
};

